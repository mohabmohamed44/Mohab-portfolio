const fontSans = FontSans({
  subsets: ["latin"],
  variable: "--font-sans",
});

const createSafeUrl = (url?: string) => {
  if (!url) return new URL("https://localhost:3000");
  try {
    return new URL(url);
  } catch {
    return new URL("https://localhost:3000");
  }
};

